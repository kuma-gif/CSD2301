### เขียนโปรแกรมที่สร้างและพิมพ์จำนวนเต็มสุ่ม 50 ตัว โดยแต่ละตัวอยู่ระหว่าง 3 ถึง 6
import random  # นำเข้าโมดูล random เพื่อใช้ในการสุ่มตัวเลข

# วนลูปเพื่อสร้างและพิมพ์ตัวเลขสุ่ม 50 ตัว
for _ in range(50): # ใช้ตัวแปร _ เป็นตัวแทนสำหรับค่าที่ไม่ใช้ (ไม่ได้ใช้ในการคำนวณภายในลูป)
    num = random.randint(3, 6)  # สุ่มตัวเลขจำนวนเต็มในช่วง 3 ถึง 6
    print(num, end=" ")  # พิมพ์ตัวเลขที่สุ่มได้และคั่นด้วยช่องว่าง
