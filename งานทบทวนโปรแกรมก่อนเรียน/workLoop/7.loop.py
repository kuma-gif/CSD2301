### เขียนโปรแกรมที่ถามชื่อผู้ใช้และพิมพ์กี่ครั้ง โปรแกรมควรพิมพ์ชื่อผู้ใช้ตามจำนวนครั้งที่กำหนด
# รับชื่อผู้ใช้จากผู้ใช้
name = input("ป้อนชื่อของคุณ: ")

# รับจำนวนครั้งที่ต้องการพิมพ์
num_times = int(input("ป้อนจำนวนครั้งที่ต้องการพิมพ์: "))

# พิมพ์ชื่อผู้ใช้ตามจำนวนครั้งที่กำหนด
for i in range(num_times):
  print(name)