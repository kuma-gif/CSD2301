#  กำหนดตัวเเปร
# numeric3: กำหนดให้เป็นตัวแปรชื่อ numeric3 และกำหนดค่าเริ่มต้นเป็น 0
numeric3 = 0 
#  เเสดงค่าตัวเเปร
# print(numeric3): แสดงค่าของตัวแปร numeric3 บนหน้าจอ ซึ่งในกรณีนี้จะแสดงค่า 0
print(numeric3) 
#  แสดงชนิดข้อมูล
# print(type(numeric3)): แสดงชนิดข้อมูลของตัวแปร numeric3 บนหน้าจอ ซึ่งในกรณีนี้จะแสดงว่า numeric3 เป็นชนิดข้อมูล int (integer)
print(type(numeric3))
