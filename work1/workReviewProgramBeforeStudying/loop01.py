### 1. เขียนโปรแกรมหาจำนวนที่หารด้วย 3 ลงตัวที่อยู่ระหว่าง 1 และ 10000 มีกี่จำนวน
a = 0 
# loop เช็คตั้งเเต่เลข 1 ถึง 10000
for i in range(1, 10001):
  # ตรวจสอบว่าจำนวนนั้นหาร 3 ลงตัวไหม
  if i % 3 == 0:
      # เพิ่มจำนวนที่หารด้วย 3 ลงตัว
      a += 1

# เเสดงผลลัพธ์
print("จำนวนที่หารด้วย 3 ลงตัวระหว่าง 1 ถึง 10000 มี", a, "จำนวน")
