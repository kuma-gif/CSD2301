### เขียนโปรแกรมหาผลรวมของจำนวนที่หารด้วย 3, 5 หรือ 7 ลงตัวที่อยู่ระหว่าง 1 และ 10000 มีกี่จำนวน
# ฟังก์ชันเพื่อหาผลรวมของจำนวนที่หารด้วย 3, 5 และ 7 ลงตัว
def sum_numbers_divisible_by_3_5_and_7():
    """
    ฟังก์ชันเพื่อหาผลรวมของจำนวนที่หารด้วย 3, 5 และ 7 ลงตัวที่อยู่ระหว่าง 1 และ 100000000

    Returns:
        ผลรวมของจำนวนที่หารด้วย 3, 5 และ 7 ลงตัว
    """
    total_sum = 0 # ตัวแปรเพื่อเก็บผลรวมของจำนวนที่หารด้วย 3, 5 และ 7 ลงตัว

    for i in range(1, 100000001): # วนลูปตั้งแต่ 1 ถึง 100000000
        if i % 3 == 0 and i % 5 == 0 and i % 7 == 0: # ตรวจสอบว่าหารด้วย 3, 5 และ 7 ลงตัว
            total_sum += i # ถ้าหารลงตัว ให้เพิ่มจำนวนนั้นลงในผลรวม

    return total_sum # คืนค่าผลรวมที่ได้

# เรียกใช้ฟังก์ชันและแสดงผลลัพธ์
result = sum_numbers_divisible_by_3_5_and_7()
print("ผลรวมของจำนวนที่หารด้วย 3, 5 และ 7 ลงตัวที่อยู่ระหว่าง 1 และ 100000000 มีทั้งหมด", result)
