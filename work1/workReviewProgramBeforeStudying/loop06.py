### เขียนโปรแกรมหาจ านวนที่หารด้วย 3, 5 หรือ 7 ลงตัวที่อยู่ระหว่าง 1 และ 10000 มีกี่จำนวน
# ฟังก์ชันเพื่อหาจำนวนที่หารด้วย 11 ลงตัว
def count_numbers_divisible_by_11():
    """
    ฟังก์ชันเพื่อหาจำนวนที่หารด้วย 11 ลงตัวที่อยู่ระหว่าง 1 และ 18000

    Returns:
        จำนวนที่หารด้วย 11 ลงตัว
    """
    count = 0  # ตัวแปรเพื่อเก็บจำนวนที่หารด้วย 11 ลงตัว

    for i in range(1, 18001):  # วนลูปตั้งแต่ 1 ถึง 18000
        if i % 11 == 0:  # ตรวจสอบว่าหารด้วย 11 ลงตัว
            count += 1  # ถ้าหารลงตัว ให้เพิ่มจำนวนขึ้น 1

    return count  # คืนค่าจำนวนที่นับได้

