### 8. เขียนโปรแกรมหาผลรวมของจำนวนที่หารด้วย 5 ลงตัวที่อยู่ระหว่าง 1 และ 10000 มีกี่จำนวน
# ฟังก์ชันเพื่อหาผลรวมของจำนวนที่หารด้วย 5 ลงตัว
def sum_numbers_divisible_by_5():
    """
    ฟังก์ชันเพื่อหาผลรวมของจำนวนที่หารด้วย 5 ลงตัวที่อยู่ระหว่าง 1 และ 10000

    Returns:
        ผลรวมของจำนวนที่หารด้วย 5 ลงตัว
    """
    total_sum = 0 # ตัวแปรเพื่อเก็บผลรวมของจำนวนที่หารด้วย 5 ลงตัว

    for i in range(1, 10001): # วนลูปตั้งแต่ 1 ถึง 10000
        if i % 5 == 0: # ตรวจสอบว่าหารด้วย 5 ลงตัว
            total_sum += i # ถ้าหารลงตัว ให้เพิ่มจำนวนนั้นลงในผลรวม

    return total_sum # คืนค่าผลรวมที่ได้

# เรียกใช้ฟังก์ชันและแสดงผลลัพธ์
result = sum_numbers_divisible_by_5()
print("ผลรวมของจำนวนที่หารด้วย 5 ลงตัวที่อยู่ระหว่าง 1 และ 10000 มีทั้งหมด", result)
