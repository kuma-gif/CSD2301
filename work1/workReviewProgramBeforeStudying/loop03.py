### 3. เขียนโปรแกรมหาจำนวนที่หารด้วย 3 และ 5 ลงตัวที่อยู่ระหว่าง 1 และ 10000 มีกี่จำนวน
value = 0 # ตัวเเปรเก็บจำนวนที่หาร 3 ,5 ลงตัว

# loop  เช็คตั้งเเต่เลข 1 ถึง 10000
for i in range(1, 10000): 
  # ตรวจสอบว่าจำนวนนั้นหารด้วย 3 เเละ 5  ลงตัวไหม
  if (i % 3 == 0) and (i % 5 == 0):
    # เพิ่มจำนวนที่หารด้วย 3 เเละ 5 ลงตัว
    value += 1

print("จำนวนที่หารด้วย 3 เเละ 5 ลงตัวระหว่าง 1 ถึง 10000มี", value,"จำนวน")
